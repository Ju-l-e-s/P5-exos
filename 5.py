def sum(a, b):
    """
    Calculate the sum of two numbers.

    :param a: The first number
    :type a: int or float
    :param b: The second number
    :type b: int or float
    :return: The sum of a and b
    :rtype: int or float
    """
    return a + b


def subtraction(a, b):
    """
    Calculate the difference between two numbers.

    :param a: The first number
    :type a: int or float
    :param b: The second number
    :type b: int or float
    :return: The difference (a - b)
    :rtype: int or float
    """
    return a - b
